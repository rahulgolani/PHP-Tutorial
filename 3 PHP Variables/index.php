<?php
// Keywords in php are NOT case sensitive - echo and ECHO are same
// Variables in php are case sensitive

// -----------------Variables---------------

// starts with a $

$name="Rahul Golani";
$profession="Software Engineer";

// PHP is a loosely typed language and it can automatically assign the type to the variable 

echo "My name is $name <br>"; //br is to be used since the code will be evaluated into html code
echo "I am a $profession <br>";


// Variable Rules-> 

// 1) Starts with a $
// 2) Cannot start with number
// 3) Must start with character or a underscore
// 4) Can only contain alphanumeric characters and number
// 5) Variable names are case sensitive

?>